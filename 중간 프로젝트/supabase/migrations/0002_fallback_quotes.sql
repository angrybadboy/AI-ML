create table public.fallback_quotes (
  id          uuid primary key default gen_random_uuid(),
  title       text not null check (char_length(title) between 1 and 60),
  body        text not null check (char_length(body) between 1 and 600),
  category    text not null
              check (category in ('고요','위로','사랑','용기','그리움','사색')),
  tags        text[] not null default '{}',
  used_count  int not null default 0,
  created_at  timestamptz not null default now()
);

alter table public.fallback_quotes enable row level security;

create policy fallback_quotes_select_authenticated
  on public.fallback_quotes for select to authenticated
  using (true);
-- INSERT/UPDATE/DELETE는 service role만 (RLS bypass).

